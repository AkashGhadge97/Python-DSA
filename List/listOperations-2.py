# + Operator
a = [1,2,3]
b = [4,5,6]
c = a+b
print(c)

# * Operator
l = [1,2]
l = l * 4
print(l)

#sum() fn
lst = [3,4,5,6,7]
print(sum(lst))

#max() fn
print(max(lst))

#min() fn
print(min(lst))

#String and List
#Convert string to a list
st = 'game'
lst = list(st)
print(lst)

#Split a string
st = 'I love india'
lst = st.split()
print(lst)

#Using a delimter to split a string
st = 'You-are-nice'
lst =  st.split('-')
print(lst)

#Using join() to join a list
print(' '.join(lst))